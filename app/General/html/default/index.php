	<div class="example" id='example_generator1' style='padding:10px; background:#fff;'>
	<p>Write your bundle definition in YAML:</p>
	<pre>
Article:
  entities:
    article:
      properties:
        title:
        content: longtext
        image: image
      relations:
        category:
          entity: Category
          has: one
      front:
        show:
      admin: true
    category:
      properties:
        title:
        content: longtext
        image: image
      relations:
        articles:
          entity: Article
          has: many
      front: true
      admin: true
	</pre>
	<p>Generate:</p>
	<pre>
php console generate bundle.yml
	</pre>
	<p>And enjoy your new bundle!</p>
