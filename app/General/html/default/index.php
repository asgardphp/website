namespace Test\Controllers

/**
 * @Prefix(&quot;admin/posts&quot;)
 */
class PostAdminController extends \Admin\Libs\Controller\EntityAdminController {
	protected $_entity = &#39;Test\Entities\Post&#39;;
	protected $_entities = &#39;posts&#39;;

	public function __construct() {
		$this-&gt;_messages = array(
			&#39;modified&#39; =&gt; __(&#39;Post modified with success.&#39;),
			&#39;created&#39; =&gt; __(&#39;Post modified with success.&#39;),
			&#39;many_deleted&#39; =&gt; __(&#39;Posts deleted with success.&#39;),
			&#39;deleted&#39; =&gt; __(&#39;Post deleted with success.&#39;),
		);
		parent::__construct();
	}

	public function formConfigure($entity) {
		return new \Admin\Libs\Form\AdminEntityForm($entity, $this);
	}
}
