			&#39;many_deleted&#39; =&gt; __(&#39;Posts deleted with success.&#39;),
			&#39;deleted&#39; =&gt; __(&#39;Post deleted with success.&#39;),
		);
		parent::__construct();
	}

	public function formConfigure($entity) {
		return new \Admin\Libs\Form\AdminEntityForm($entity, $this);
	}
}
